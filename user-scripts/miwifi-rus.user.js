  function findAndReplace(searchText, replacement, searchNode) {
    if (!searchText || typeof replacement === 'undefined') {
      return;
    }
    const regex = typeof searchText === 'string' ? new RegExp(searchText, 'g') : searchText;
    const childNodes = (searchNode || document.body).childNodes;
    const excludes = ['html', 'head', 'style', 'title', 'link', 'meta', 'script', 'object', 'iframe'];
    for (let i = 0; i < childNodes.length; i++) {
      const currentNode = childNodes[i];
      if (currentNode.nodeType === 1 && !excludes.includes(currentNode.nodeName.toLowerCase())) {
        findAndReplace(searchText, replacement, currentNode);
      }
      if (currentNode.nodeType !== 3 || !regex.test(currentNode.data)) {
        continue;
      }
      const parent = currentNode.parentNode;
      const frag = document.createDocumentFragment();
      const html = currentNode.data.replace(regex, replacement);
      const wrap = document.createElement('div');
      wrap.innerHTML = html;
      while (wrap.firstChild) {
        frag.appendChild(wrap.firstChild);
      }
      parent.insertBefore(frag, currentNode);
      parent.removeChild(currentNode);
    }
  }
