  function replaceNodeValue(currentNode, replacementsList) {
    for (const [regex, replacement] of replacementsList) {
      if (regex.test(currentNode.data)) {
        currentNode.data = currentNode.data.replace(regex, replacement);
      }
    }
  }

  const EXCLUDE_NODES = ['html', 'head', 'style', 'title', 'link', 'meta', 'script', 'object', 'iframe'];

  function findAndReplace(searchNode) {
    const childNodes = (searchNode || document.body).childNodes;

    for (const currentNode of childNodes) {
      if (currentNode.nodeType === ELEMENT_NODE && !EXCLUDE_NODES.includes(currentNode.nodeName.toLowerCase())) {
        findAndReplace(currentNode);
      }
      if (currentNode.nodeType === TEXT_NODE) {
        replaceNodeValue(currentNode, TS);
      }
    }

    const passwordInput = document.querySelector('[type="password"]');
    if (passwordInput) passwordInput.placeholder = 'Password';
  }

  function translate() {
    findAndReplace();
    setTimeout(translate, 200);
  }
  setTimeout(translate, 200);
